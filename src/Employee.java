class Employee {

     private String name;
     private String post;
     private String email;
     private String phone;
     private int age;

     public Employee(String name, String post, String email, String phone, int age) {
          this.name = name;
          this.post = post;
          this.email = email;
          this.phone = phone;
          this.age = age;



     }
}
