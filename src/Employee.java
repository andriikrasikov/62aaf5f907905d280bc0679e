class Employee {

    private final String name;
    private final String post;
    private final String email;
    private final String phone;
    private final int age;

    public Employee(String name, String post, String email, String phone, int age) {
        this.name = name;
        this.post = post;
        this.email = email;
        this.phone = phone;
        this.age = age;


    }
}
